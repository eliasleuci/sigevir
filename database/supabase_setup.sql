-- 6. ROW LEVEL SECURITY (RLS)
-- ##############################

-- Habilitar RLS en perfiles
ALTER TABLE perfiles ENABLE ROW LEVEL SECURITY;

-- Pol├¡tica 1: Admin puede ver y gestionar todo
CREATE POLICY admin_perfiles_all ON perfiles
  FOR ALL
  TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM perfiles
      WHERE id = auth.uid() AND rol = 'admin'
    )
  )
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM perfiles
      WHERE id = auth.uid() AND rol = 'admin'
    )
  );

-- Pol├¡tica 2: Usuario puede ver su propio perfil
CREATE POLICY usuario_perfiles_self ON perfiles
  FOR SELECT
  TO authenticated
  USING (id = auth.uid());

-- Pol├¡tica 3: fiscal_juez puede ver todos los perfiles
CREATE POLICY fiscal_juez_perfiles_select ON perfiles
  FOR SELECT
  TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM perfiles
      WHERE id = auth.uid() AND rol = 'fiscal_juez'
    )
  );

-- Pol├¡tica 4: agente_campo y deposito ven perfiles de su misma instituci├│n
CREATE POLICY agente_deposito_perfiles_select ON perfiles
  FOR SELECT
  TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM perfiles p
      WHERE p.id = auth.uid()
      AND p.rol IN ('agente_campo', 'deposito')
      AND p.institucion = perfiles.institucion
    )
  );

-- Habilitar RLS en tipos_personal
ALTER TABLE tipos_personal ENABLE ROW LEVEL SECURITY;

-- Todos los usuarios autenticados pueden leer tipos_personal
CREATE POLICY tipos_personal_select ON tipos_personal
  FOR SELECT
  TO authenticated
  USING (true);

-- Solo admin puede modificar tipos_personal
CREATE POLICY tipos_personal_admin ON tipos_personal
  FOR ALL
  TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM perfiles
      WHERE id = auth.uid() AND rol = 'admin'
    )
  )
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM perfiles
      WHERE id = auth.uid() AND rol = 'admin'
    )
  );

