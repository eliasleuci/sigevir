-- ##############################
-- 2. TABLA: perfiles
-- ##############################
CREATE TABLE IF NOT EXISTS perfiles (
  id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
  nombre_completo VARCHAR(255) NOT NULL,
  dni VARCHAR(20) NOT NULL,
  email VARCHAR(255) NOT NULL,
  telefono VARCHAR(50),
  institucion VARCHAR(255) NOT NULL,
  jurisdiccion VARCHAR(255),
  cargo VARCHAR(255),
  tipo_personal_id BIGINT REFERENCES tipos_personal(id),
  rol VARCHAR(50) NOT NULL DEFAULT 'agente_campo',
  activo BOOLEAN DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

COMMENT ON TABLE perfiles IS 'Perfiles de usuario del sistema SIGEVIR, vinculados a auth.users';
COMMENT ON COLUMN perfiles.id IS 'Misma UUID que auth.users.id';
COMMENT ON COLUMN perfiles.rol IS 'Rol asignado autom├íticamente seg├║n tipo_personal';

-- ##############################
-- 3. FUNCI├ôN TRIGGER: handle_new_user
-- ##############################
CREATE OR REPLACE FUNCTION handle_new_user()
RETURNS TRIGGER
SECURITY DEFINER SET search_path = public
LANGUAGE plpgsql
AS 
DECLARE
  v_tipo_personal_id BIGINT;
  v_rol VARCHAR(50);
  v_nombre_completo TEXT;
  v_dni TEXT;
  v_telefono TEXT;
  v_cargo TEXT;
  v_institucion TEXT;
  v_jurisdiccion TEXT;
BEGIN
  -- Extraer metadata del registro en auth.users
  v_nombre_completo := NEW.raw_user_meta_data ->> 'nombre_completo';
  v_dni := NEW.raw_user_meta_data ->> 'dni';
  v_telefono := NEW.raw_user_meta_data ->> 'telefono';
  v_cargo := NEW.raw_user_meta_data ->> 'cargo';
  v_institucion := NEW.raw_user_meta_data ->> 'institucion';
  v_jurisdiccion := NEW.raw_user_meta_data ->> 'jurisdiccion';
  v_tipo_personal_id := (NEW.raw_user_meta_data ->> 'tipo_personal_id')::BIGINT;

  -- Obtener el rol asignado seg├║n el tipo de personal
  SELECT rol_asignado INTO v_rol
  FROM tipos_personal
  WHERE id = v_tipo_personal_id;

  -- Si no se encontr├│ rol, asignar agente_campo por defecto
  IF v_rol IS NULL THEN
    v_rol := 'agente_campo';
  END IF;

  -- Crear perfil
  INSERT INTO public.perfiles (
    id, nombre_completo, dni, email, telefono,
    institucion, jurisdiccion, cargo,
    tipo_personal_id, rol, activo
  ) VALUES (
    NEW.id,
    COALESCE(v_nombre_completo, 'Sin nombre'),
    COALESCE(v_dni, '00000000'),
    NEW.email,
    v_telefono,
    COALESCE(v_institucion, 'Sin instituci├│n'),
    v_jurisdiccion,
    v_cargo,
    v_tipo_personal_id,
    COALESCE(v_rol, 'agente_campo'),
    true
  );

  RETURN NEW;
END;
;

-- Trigger: ejecutar handle_new_user despu├®s de crear un usuario en auth.users
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW
  EXECUTE FUNCTION handle_new_user();

-- ##############################
-- 4. FUNCI├ôN: actualizar updated_at
-- ##############################
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS 
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
;

CREATE TRIGGER perfiles_updated_at
  BEFORE UPDATE ON perfiles
  FOR EACH ROW
  EXECUTE FUNCTION update_updated_at();

